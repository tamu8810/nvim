-- lazy setup
local lazypath = vim.fn.stdpath("data") .. "/lazy/lazy.nvim"
if not vim.loop.fs_stat(lazypath) then
    vim.fn.system({
        "git",
        "clone",
        "--filter=blob:none",
        "https://github.com/folke/lazy.nvim.git",
        "--branch=stable", -- latest stable release
        lazypath,
    })
end
vim.opt.rtp:prepend(lazypath)

local config = {}

config.install = {
    missing = false
}

config.checker = {
    enabled = true,
    notify = false
}

config.change_detection = {
    notify = false
}

config.readme = {
    enabled = false,
    skip_if_doc_exists = false
}

-- plugin load
require("lazy").setup({ import = "plug" }, config)
